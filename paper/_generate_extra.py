# Figure 1: NSI vs window-mean VIX scatter with OLS line + 95% CI.
# Uses the same data as fig5_nsi_vix_overlay but renders the correlation
# directly so the reader can verify Pearson r visually.
# ---------------------------------------------------------------------
def figure_nsi_vix_scatter(out_path):
    s5 = _load_stage5()
    nsi = s5["snapshot_nsi"].copy()
    nsi = nsi.set_index("snapshot").reindex(SNAP_ORDER).reset_index()
    vix_s = _load_vix()
    # Window-mean VIX over each snapshot's own estimation window (from
    # config), so the scatter cannot drift from the panel.
    win_vix = []
    for lbl in nsi["snapshot"]:
        a, b = SNAP_WINDOWS[lbl]
        s = vix_s.loc[a:b]
        win_vix.append(float(s.mean()))
    nsi["vix_mean"] = win_vix

    x = nsi["vix_mean"].to_numpy()
    y = nsi["nsi"].to_numpy()
    assert len(x) == len(SNAP_ORDER) and len(y) == len(SNAP_ORDER), \
        f"NSI-VIX scatter expects {len(SNAP_ORDER)} points"

    # OLS regression line + analytic 95% prediction band on the mean.
    slope, intercept = np.polyfit(x, y, deg=1)
    resid = y - (slope * x + intercept)
    n = len(x)
    s2 = float(np.sum(resid ** 2)) / max(1, n - 2)
    xbar = float(np.mean(x))
    sxx = float(np.sum((x - xbar) ** 2))
    xx = np.linspace(x.min() - 1, x.max() + 1, 200)
    se = np.sqrt(s2 * (1.0 / n + (xx - xbar) ** 2 / sxx))
    from scipy.stats import t as student_t
    tcrit = float(student_t.ppf(0.975, df=n - 2))
    yy = slope * xx + intercept

    r = float(np.corrcoef(x, y)[0, 1])

    fig, ax = plt.subplots(figsize=(5.2, 4.0))
    ax.fill_between(xx, yy - tcrit * se, yy + tcrit * se,
                    color=IMPERIAL_GOLD_LT, alpha=0.22, zorder=1,
                    label="OLS 95% mean-band")
    ax.plot(xx, yy, color=IMPERIAL_GRAPHITE, linewidth=1.4, zorder=2,
            label=f"OLS  NSI = {slope:.3f}$\\cdot$VIX + {intercept:.2f}")

    for _, row in nsi.iterrows():
        c = REGIME_COLORS[row["regime"]]
        ax.scatter(row["vix_mean"], row["nsi"], s=65, color=c,
                   edgecolor=IMPERIAL_GRAPHITE, linewidth=0.7, zorder=4)
        ax.annotate(SHORT_LABEL[row["snapshot"]],
                    (row["vix_mean"], row["nsi"]),
                    xytext=(5, 3), textcoords="offset points",
                    fontsize=6.8, color=IMPERIAL_GRAPHITE)

    ax.set_xlabel("Window-mean VIX", fontweight="bold")
    ax.set_ylabel("Snapshot NSI", fontweight="bold")
    ax.set_title(f"NSI vs. VIX (snapshot, $n={len(SNAP_ORDER)}$): "
                 f"Pearson $r = {r:.3f}$", fontweight="bold")
    ax.grid(linestyle=":", linewidth=0.4, alpha=0.5)
    ax.set_axisbelow(True)

    # Regime legend.
    legend_handles = [mpatches.Patch(color=c, label=k.capitalize())
                      for k, c in REGIME_COLORS.items()]
    ax.legend(handles=legend_handles, loc="lower right",
              fontsize=7, frameon=True, framealpha=0.95,
              facecolor=IMPERIAL_IVORY, edgecolor=IMPERIAL_GOLD_LT,
              ncol=2)

    fig.tight_layout()
    fig.savefig(out_path)
    fig.savefig(out_path.with_suffix(".png"), dpi=300)
    plt.close(fig)
    print(f"  wrote {out_path} ({out_path.stat().st_size:,} bytes)")


# ---------------------------------------------------------------------
# Figure 2: Exact-permutation power lattice. The null has C(N, k) parts
# for N = len(SNAP_ORDER) snapshots and k crisis windows (post-CRSP:
# C(20, 7) = 77,520, hard floor 1/77,520 ~ 1.3e-5). Every
# positively-deviating headline metric (Cohen's d > 0) is plotted at its
# observed (d, p) from inference_macros.tex against the p = 0.05 / 0.10
# reference lines and the floor. The metric membership is read from the
# macros, so a sign change (e.g. the HHI un-reversal under CRSP) flows
# through automatically rather than via a hand-edited inclusion list.
# ---------------------------------------------------------------------
def figure_power_mde(out_path):
    from math import comb

    n_snap = len(SNAP_ORDER)
    n_crisis = sum(1 for s in SNAPSHOTS if s[3] == "crisis")
    n_part = comb(n_snap, n_crisis)
    floor = 1.0 / n_part

    macros_path = ROOT / "paper" / "output" / "inference_macros.tex"
    macros = {}
    if macros_path.exists():
        import re
        macro_re = re.compile(r"\\newcommand\{\\([A-Za-z]+)\}\{([^}]*)\}")
        for line in macros_path.read_text(encoding="utf-8").splitlines():
            mm = macro_re.match(line.strip())
            if mm:
                macros[mm.group(1)] = mm.group(2)

    metrics = [
        ("Mut.-dyad frac.",   "DMutDyad", "PMutDyad"),
        ("PageRank Gini",     "DGini",    "PGini"),
        ("Mean $\\bar\\rho$", "DMeanRho", "PMeanRho"),
        ("Cross-sec. edges",  "DCrossSec", "PCrossSec"),
        ("$|Z_\\text{MR}|$",  "DZMR",     "PZMR"),
        ("PageRank HHI",      "DHHI",     "PHHI"),
        ("NSI",               "DNSI",     "PNSI"),
    ]
    pts = []
    for label, dk, pk in metrics:
        dv = macros.get(f"Inf{dk}")
        pv = macros.get(f"Inf{pk}")
        if dv is None or pv is None:
            continue
        try:
            d, p = float(dv), float(pv)
        except ValueError:
            continue
        if d > 0:                       # positively-deviating metrics only
            pts.append((label, d, p))
    assert pts, "no positively-deviating inference macros for the MDE figure"
    pts.sort(key=lambda t: t[1])        # by d, for stable label placement

    fig, ax = plt.subplots(figsize=(5.4, 4.0))

    # p = 0.05 / 0.10 reference lines.
    for thr_p, lab, ls in [(0.05, r"$p=0.05$", "--"),
                           (0.10, r"$p=0.10$", ":")]:
        ax.axhline(thr_p, color=REGIME_COLORS["crisis"],
                   linewidth=0.9, linestyle=ls, alpha=0.85)
        ax.text(0.015, thr_p + 0.006, lab, fontsize=7,
                color=REGIME_COLORS["crisis"], va="bottom")

    # Hard floor at 1/C(N, k). On the 20-snapshot panel this is ~1.3e-5,
    # far below every observed p: the binding constraint is now the data,
    # not the partition count (the opposite of the old 10-snapshot panel,
    # whose 1/45 floor sat among the observations).
    exp = int(np.floor(np.log10(floor)))
    mant = floor / 10 ** exp
    n_part_tex = f"{n_part:,}".replace(",", "{,}")
    ax.axhline(floor, color=REGIME_COLORS["recovery"],
               linewidth=0.9, linestyle="-", alpha=0.9)
    ax.text(0.015, floor + 0.004,
            rf"hard floor $1/\binom{{{n_snap}}}{{{n_crisis}}}="
            rf"1/{n_part_tex}\approx {mant:.1f}\times10^{{{exp}}}$",
            fontsize=7, color=REGIME_COLORS["recovery"], va="bottom")

    for i, (label, d, p) in enumerate(pts):
        ax.scatter(d, p, s=70, color=REGIME_COLORS["baseline"],
                   edgecolor=IMPERIAL_GOLD_LT, linewidth=0.9, zorder=5)
        dy = 11 if (i % 2 == 0) else -17
        ax.annotate(label, (d, p), xytext=(6, dy),
                    textcoords="offset points", fontsize=7.0,
                    color=IMPERIAL_GRAPHITE,
                    bbox=dict(boxstyle="round,pad=0.18",
                              facecolor=IMPERIAL_IVORY,
                              edgecolor="none", alpha=0.92))

    pmax = max(p for _l, _d, p in pts)
    ax.set_xlabel("Cohen's $d$  (crisis vs. non-crisis)", fontweight="bold")
    ax.set_ylabel("Exact-permutation $p$", fontweight="bold")
    ax.set_title(f"Power lattice: positively-deviating metrics on the "
                 f"$\\binom{{{n_snap}}}{{{n_crisis}}}={n_part_tex}$ null",
                 fontweight="bold")
    ax.set_xlim(left=0)
    ax.set_ylim(-0.01, max(0.40, pmax * 1.18))
    ax.grid(linestyle=":", linewidth=0.4, alpha=0.45)
    ax.set_axisbelow(True)
    fig.tight_layout()
    fig.savefig(out_path)
    fig.savefig(out_path.with_suffix(".png"), dpi=300)
    plt.close(fig)
    print(f"  wrote {out_path} ({out_path.stat().st_size:,} bytes)")


