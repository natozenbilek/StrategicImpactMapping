    out.append("\\label{tab:stage3-sensitivity}\n")
    out.append("\\centering\n")
    out.append("\\small\n")
    out.append("\\rowcolors{2}{imperialCream}{white}\n")
    out.append("\\begin{tabular}{@{}rrrrrrr@{}}\n")
    out.append("\\toprule\n")
    out.append("$\\alpha$ & $\\tau$ & lag & $c_\\text{floor}$ & "
               "$\\overline{f}_\\text{mut}^\\text{cris}$ & "
               "$\\overline{f}_\\text{mut}^\\text{ncri}$ & "
               "$\\Delta$ \\\\\n")
    out.append("\\midrule\n")
