def figure_cross_sector_matrix(out_path):
    s4 = _load_stage4()
    info = pd.read_parquet(ROOT / "data" / "sp500_info.parquet")
    sector_of = dict(zip(info["Symbol"], info["GICS Sector"]))
    sectors = sorted(set(info["GICS Sector"].dropna()))
    n_s = len(sectors)
    idx_of = {s: i for i, s in enumerate(sectors)}

    # Pool the two crisis-peak Stage-3 graphs (rounded to the top-4637
    # edges by |partial correlation| each). For each kept edge increment
    # the sector-pair cell.
    crisis_labels = ["Oct 2008 GFC", "Mar 2020 COVID"]
    mat = np.zeros((n_s, n_s), dtype=float)
    for lbl in crisis_labels:
        d = s4[lbl]
        G = d["graph"]
        tickers = d["tickers"]
        # All edges with weights; pick top-4637.
        edges = []
        for u, v in G.edges():
            w = abs(G[u][v].get("weight", 1.0))
            edges.append((u, v, w))
        edges.sort(key=lambda t: t[2], reverse=True)
        edges = edges[:4637]
        for u, v, _w in edges:
            tu = tickers[u] if u < len(tickers) else None
            tv = tickers[v] if v < len(tickers) else None
            su = sector_of.get(tu)
            sv = sector_of.get(tv)
            if su is None or sv is None:
                continue
            i = idx_of[su]; j = idx_of[sv]
            mat[i, j] += 1
    # Row-normalise so each row sums to 1 (read as "given the source
    # sector, where do its outgoing edges land?").
    row_sums = mat.sum(axis=1, keepdims=True)
    row_sums[row_sums == 0] = 1.0
    norm_mat = mat / row_sums

    fig, ax = plt.subplots(figsize=(6.2, 5.4))
    im = ax.imshow(norm_mat, cmap="viridis", vmin=0, vmax=0.45, aspect="auto")
    ax.set_xticks(range(n_s))
    ax.set_xticklabels([s.replace(" ", "\n", 1) for s in sectors],
                       rotation=70, ha="right", fontsize=7)
    ax.set_yticks(range(n_s))
    ax.set_yticklabels(sectors, fontsize=7.5)
    # Outline the diagonal (within-sector cells).
    for i in range(n_s):
        ax.add_patch(plt.Rectangle((i - 0.5, i - 0.5), 1, 1,
                                   fill=False,
                                   edgecolor=IMPERIAL_GOLD_LT,
                                   linewidth=1.0))
    ax.set_xlabel("Target sector", fontweight="bold")
    ax.set_ylabel("Source sector", fontweight="bold")
    ax.set_title("Cross-sector edge incidence at top-$k{=}4637$ "
                 "(crisis-peak pool)", fontweight="bold")
    cb = plt.colorbar(im, ax=ax, shrink=0.85)
    cb.set_label("Row-normalised incidence", fontweight="bold")
    fig.tight_layout()
    fig.savefig(out_path)
    fig.savefig(out_path.with_suffix(".png"), dpi=300)
    plt.close(fig)
    print(f"  wrote {out_path} ({out_path.stat().st_size:,} bytes)")


