# Table 1: Bonferroni per-snapshot Z on Q1 + three motif axes.
# Two-sided Bonferroni at family size m = len(SNAP_ORDER) (per axis):
# Phi^{-1}(1 - 0.025/m). On the post-CRSP 20-snapshot panel m = 20 and
# the threshold is ~3.023. The Q1 (ER clustering) axis clears on every
# snapshot; >=1 of the three motif axes dips below it on the early-panel windows,
# which are daggered and named in the caption rather than masked (the
# pre-CRSP 10-snapshot panel had no failures, hence the old caption).
# ---------------------------------------------------------------------
def emit_bonf_z_table(out_path):
    s4 = _load_stage4()
    n_fam = len(SNAP_ORDER)
    thr = float(norm.ppf(1 - 0.025 / n_fam))   # per-axis Bonferroni-m

    rows = []
    for label in SNAP_ORDER:
        d = s4[label]
        zc   = abs(d["erdos_renyi"]["z_scores"]["clustering"])
        mo   = d.get("motifs")
        zffl = abs(mo["z_scores"]["feed_forward_loop"]) if mo else float("nan")
        zmr  = abs(mo["z_scores"]["mutual_regulation"])  if mo else float("nan")
        zsim = abs(mo["z_scores"]["single_input_module"]) if mo else float("nan")
        rows.append((label, d["regime"], zc, zffl, zmr, zsim))

    # Q1 (ER clustering) is the panel-wide robust claim: it must clear the
    # threshold on every snapshot. The motif axes are permitted to fall
    # below on sparse windows; those cells are daggered, not masked.
    for label, _r, zc, *_ in rows:
        assert zc > thr, (
            f"clustering |Z_C|={zc:.2f} at {label} fell below the "
            f"Bonferroni-{n_fam} threshold {thr:.3f}; the panel-wide Q1 "
            "claim no longer holds -- revise the prose, do not mask it."
        )

    # Catalogue cells below the threshold and the single smallest |Z|
    # across all 4*m axis-snap pairs, for a data-driven caption.
    axis_sym = {"Q1": "|Z_C|", "FFL": "|Z_\\text{FFL}|",
                "MR": "|Z_\\text{MR}|", "SIM": "|Z_\\text{SIM}|"}
    cells = [(label, ax, v)
             for (label, _r, zc, zffl, zmr, zsim) in rows
             for ax, v in (("Q1", zc), ("FFL", zffl),
                           ("MR", zmr), ("SIM", zsim))
             if np.isfinite(v)]
    fail_snaps = sorted({label for label, _ax, v in cells if v <= thr},
                        key=SNAP_ORDER.index)
    gmin = min(cells, key=lambda t: t[2])
    zc_lo = min(r[2] for r in rows)
    zc_hi = max(r[2] for r in rows)
    fail_names = ", ".join(SHORT_LABEL[L].replace(" ", "~") for L in fail_snaps)

    out = [BANNER_HEAD]
    out.append(f"% Bonferroni-{n_fam} per-snapshot |Z| table.\n")
    out.append("\\begin{table}[htbp]\n")
    out.append("\\caption{Per-snapshot $|Z|$ on the Q1 (ER clustering) "
               "and three motif axes against the per-axis "
               f"Bonferroni-{n_fam} two-sided threshold "
               f"$|Z|>\\Phi^{{-1}}(1{{-}}0.025/{n_fam})\\approx {thr:.3f}$ "
               "($\\dagger$ marks cells below it). Q1 clears on every "
               f"snapshot ($|Z_C|\\in[{zc_lo:.2f},\\,{zc_hi:.2f}]$), while "
               "at least one of the three motif axes dips below the "
               f"threshold on the early-panel windows {fail_names}; the smallest cell is "
               f"${axis_sym[gmin[1]]}={gmin[2]:.2f}$ at "
               f"{gmin[0].replace(' ', '~')}, the minimum across all "
               f"{len(cells)} axis--snap pairs. Auto-extracted from "
               "\\texttt{stage4\\_results.pkl}.}\n")
    out.append("\\label{tab:bonf-z-per-snapshot}\n")
    out.append("\\centering\n")
    out.append("\\small\n")
    out.append("\\rowcolors{2}{imperialCream}{white}\n")
    out.append("\\begin{tabular}{@{}llrrrr@{}}\n")
    out.append("\\toprule\n")
    out.append("Snapshot & Regime & $|Z_C|$ & $|Z_\\text{FFL}|$ & "
               "$|Z_\\text{MR}|$ & $|Z_\\text{SIM}|$ \\\\\n")
    out.append("\\midrule\n")

    def _cell(v):
        if not np.isfinite(v):
            return "{--}"
        return f"{v:.2f}$^\\dagger$" if v <= thr else f"{v:.2f}"

    for label, regime, zc, zffl, zmr, zsim in rows:
        out.append(
            f"{SHORT_LABEL[label]:<10s} & {regime:<8s} & "
            f"{_cell(zc)} & {_cell(zffl)} & {_cell(zmr)} & {_cell(zsim)} \\\\\n"
        )
    out.append("\\bottomrule\n")
    out.append("\\end{tabular}\n")
    out.append("\\end{table}\n")
    out_path.write_text("".join(out), encoding="utf-8")
    print(f"  wrote {out_path} ({out_path.stat().st_size:,} bytes)")


