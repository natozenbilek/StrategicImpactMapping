_MONTHS = {"Jan", "Feb", "Mar", "Apr", "May", "Jun",
           "Jul", "Aug", "Sep", "Oct", "Nov", "Dec"}


def _short_label(label: str) -> str:
    """Leading date token(s) of a snapshot label, for tight table/axis text.

    'Oct 1987 Black Monday' -> 'Oct 1987'; '1993 Calm' -> '1993';
    'Q4 2018 VolShock' -> 'Q4 2018'; '1990-91 Recession' -> '1990-91'.
    """
    toks = label.split()
    if toks[0] in _MONTHS or toks[0].startswith("Q"):
        return f"{toks[0]} {toks[1]}"
    return toks[0]


